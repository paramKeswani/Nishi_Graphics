  <script>
  const counters = document.querySelectorAll('.counter');
const speed = 200; // The lower the slower

// Function to check if an element is in the viewport
const isInViewport = (element) => {
    if (!element) {
        return false;
    }
    
    const rect = element.getBoundingClientRect();
    return (
        rect.top >= 0 &&
        rect.left >= 0 &&
        rect.bottom <= (window.innerHeight || document.documentElement.clientHeight) &&
        rect.right <= (window.innerWidth || document.documentElement.clientWidth)
    );
};

// Function to update the count for a specific counter
const updateCount = (counter) => {
    const target = +counter.getAttribute('data-target');
    const count = +counter.innerText;
    const inc = target / speed;

    if (count < target) {
        counter.innerText = Math.ceil(count + inc); // Round up to ensure the target is reached
        setTimeout(() => updateCount(counter), 1);
    } else {
        counter.innerText = target;
    }
};

// Function to check if the counters section is in view and start counting
const checkCountersInView = () => {
    const countersSection = document.querySelector('.counters');

    if (isInViewport(countersSection)) {
        counters.forEach(updateCount);
        window.removeEventListener('scroll', checkCountersInView); // Remove the listener once counting starts
    }
};

// Listen for the scroll event to check when the counters section is in view
window.addEventListener('scroll', checkCountersInView);

// Initial check when the page loads
checkCountersInView();



  </script>

